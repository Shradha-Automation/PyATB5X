# Lambda (always return a function and we call that function)

def add(n):
    return n + 21
result = lambda n: n+21
print (result(51))

def sub(n):
    return n - 3
result = lambda n: n-3
print (result (10))

def mul (n):
    return n * 2
result = lambda n : n * 2
print (result(17))

def div(n):
    return n / 1
result = lambda n : n/1
print (result (7))

def mod (n):
    return n % 5
result = lambda n : n % 5
print (result (79))